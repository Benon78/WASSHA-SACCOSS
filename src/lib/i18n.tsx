const dict = { en, sw } as const;

type Key = keyof typeof en;

interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (k: Key) => string;
}

const Ctx = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() =>
    typeof window !== "undefined" ? (localStorage.getItem("lang") as Lang) || "en" : "en",
  );
  useEffect(() => {
    try {
      localStorage.setItem("lang", lang);
      if (typeof document !== "undefined") {
        document.documentElement.lang = lang;
      }
    } catch (e) {
      console.error(e);
    }
  }, [lang]);
  const value: I18nCtx = {
    lang,
    setLang: setLangState,
    t: (k) =>
      (dict[lang] as Record<string, string>)[k] ?? (dict.en as Record<string, string>)[k] ?? k,
  };
  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useI18n() {
  const c = useContext(Ctx);
  if (!c) return { lang: "en" as Lang, setLang: () => {}, t: (k: string) => k };
  return c;
}
